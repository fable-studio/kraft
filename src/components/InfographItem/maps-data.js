const formatCSV = csv => {
  if (!csv) return [];

  let cols = csv[0] || [],
    parsedCSVAr = [['Id', 'Value']],
    idIndex,
    i,
    valueIndex;

  for (i = 0; i < cols.length; i++) {
    if (cols[i] && (cols[i].toLowerCase() === 'id')) {
      idIndex = i;
    } else if (cols[i] && (cols[i].toLowerCase() === 'value')) {
      valueIndex = i;
    }
  }
  for (i = 1; i < csv.length; i++) {
    parsedCSVAr.push([
      csv[i][idIndex],
      csv[i][valueIndex]
    ]);
  }

  return parsedCSVAr;
};

